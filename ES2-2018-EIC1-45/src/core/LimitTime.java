package core;

/**
 * @author Tiago Almeida
 *
 */
public class LimitTime {

	private String time;
	
	/**
	 * The constructor
	 * 
	 * @param time
	 */
	public LimitTime(String time) {
		this.time = time;
	}

	/**
	 * Returns the time
	 * 
	 * @return time
	 */
	public String getTime() {
		return time;
	}

	/**
	 * Set a time
	 * 
	 * @param time
	 */
	public void setTime(String time) {
		this.time = time;
	}
	
	
}
